#!/usr/bin/env node
/**
 * Enrich existing leads with investor-grade features
 * Usage: node enrich-investor.js [--export-app] [--limit N] [--no-geo]
 */

const fs = require('fs');
const { scoreLead } = require('./scraper/scoring');
const {
  buildOwnerIndex,
  enrichInvestorFeatures,
  enrichInvestorFeaturesLocal,
  createGeoCache
} = require('./scraper/investor-features');

function parseArgs(argv) {
  const options = {
    input: 'clearwater-leads.json',
    output: 'clearwater-leads.json',
    exportApp: false,
    limit: null,
    fetchGeo: true
  };

  for (let i = 0; i < argv.length; i += 1) {
    const arg = argv[i];
    if (arg === '--input' && argv[i + 1]) options.input = argv[++i];
    else if (arg === '--output' && argv[i + 1]) options.output = argv[++i];
    else if (arg === '--limit' && argv[i + 1]) options.limit = Number(argv[++i]);
    else if (arg === '--export-app') options.exportApp = true;
    else if (arg === '--no-geo') options.fetchGeo = false;
    else if (arg === '--help') {
      console.log(`
Investor feature enrichment

Options:
  --input FILE    Input JSON (default: clearwater-leads.json)
  --output FILE   Output JSON (default: same as input)
  --limit N       Only enrich first N leads
  --export-app    Regenerate leads-data.js
  --no-geo        Skip elevation/road/amenity API lookups (instant local metrics only)

Adds: $/acre, lot depth, elevation, road access, highway distance, zoning context,
owner portfolio count, subdivision potential, utility flags, development potential,
investor signals, and risk flags.
`);
      process.exit(0);
    }
  }

  return options;
}

function exportForApp(leads) {
  const code = `// Auto-generated by enrich-investor.js\nconst leadData = ${JSON.stringify(leads, null, 2)};\n\ndocument.addEventListener('DOMContentLoaded', () => {\n  if (typeof prospects !== 'undefined') {\n    prospects = leadData;\n    if (typeof loadSaved === 'function') loadSaved();\n    if (typeof buildLeadFilters === 'function') buildLeadFilters();\n    if (typeof loadParcelBoundaries === 'function') loadParcelBoundaries(prospects);\n  }\n});\n`;
  fs.writeFileSync('leads-data.js', code);
}

async function main() {
  const options = parseArgs(process.argv.slice(2));
  const leads = JSON.parse(fs.readFileSync(options.input, 'utf8'));
  const targets = options.limit ? leads.slice(0, options.limit) : leads;
  const ownerIndex = buildOwnerIndex(leads);
  const cache = createGeoCache();

  console.log(`\n📈 Enriching ${targets.length} leads with investor features`);
  console.log(`   Geo lookups: ${options.fetchGeo ? 'yes (cached by ~1km grid)' : 'no'}\n`);

  let geoLookups = 0;
  for (let i = 0; i < targets.length; i += 1) {
    const lead = targets[i];
    process.stdout.write(`\r  ${i + 1}/${targets.length} — ${(lead.owner || '').slice(0, 28).padEnd(28)}`);

    const before = options.fetchGeo ? null : undefined;
    const enriched = options.fetchGeo
      ? await enrichInvestorFeatures(lead, { ownerIndex, cache, fetchGeo: true })
      : { ...lead, ...enrichInvestorFeaturesLocal(lead, ownerIndex) };

    if (options.fetchGeo) geoLookups = cache.geo.size;

    Object.assign(lead, enriched);
    const { score, breakdown } = scoreLead(lead);
    lead.score = score;
    lead.scoreBreakdown = breakdown;
  }

  process.stdout.write('\n');
  leads.sort((a, b) => b.score - a.score || a.landValue - b.landValue);
  leads.forEach((lead, idx) => { lead.id = idx + 1; });

  fs.writeFileSync(options.output, JSON.stringify(leads, null, 2));

  const highDev = leads.filter((l) => l.developmentPotential === 'High').length;
  const portfolio = leads.filter((l) => l.isPortfolioOwner).length;
  const lowAcre = leads.filter((l) => l.pricePerAcre && l.pricePerAcre <= 15000).length;

  console.log('\n' + '='.repeat(60));
  console.log('✅ Investor enrichment complete');
  console.log(`   High development potential: ${highDev}/${leads.length}`);
  console.log(`   Portfolio owners (2+ parcels): ${portfolio}`);
  console.log(`   Under $15K/acre: ${lowAcre}`);
  if (options.fetchGeo) console.log(`   Unique geo cache entries: ${geoLookups}`);
  console.log(`   Saved → ${options.output}`);
  console.log('='.repeat(60));

  if (options.exportApp) {
    exportForApp(leads);
    console.log('✅ Wrote leads-data.js');
  }
}

main().catch((err) => {
  console.error('❌', err.message);
  process.exit(1);
});